var pacman = {
	x: 2,
	y: 2
}

var score = 0;

function displayWorld() {
	var output = '';

	for (var i = 0; i<world.length; i++){
		output += "\n<div class='row'>\n"
		for(var j = 0; j<world[i].length; j++){
			if(world[i][j] == 2){
				output += "<div class='brick'></div>"
			} else if(world[i][j] == 1){
				output += "<div class='coin'></div>"
			} else if(world[i][j] == 0){
				output += "<div class='empty'></div>"
			} else if(world[i][j] == 7){
				output += "<div class='invWall'></div>"
			}
		}
		output += "\n</div>"
	}
	document.getElementById('world').innerHTML = output;
};

function displayPacman() {
	document.getElementById('pacman').style.top = ((pacman.y*25)+3)+"px"
	document.getElementById('pacman').style.left = ((pacman.x*25)+2)+"px"
}

document.onkeydown = function(e) {
	if(e.keyCode == 37 && ((world[pacman.y][pacman.x-1] !=2) && (world[pacman.y][pacman.x-1] !=7))) {
		console.log('left')
		pacman.x--;
	} else if (e.keyCode == 39 && ((world[pacman.y][pacman.x+1] !=2) && (world[pacman.y][pacman.x+1] !=7))) {
		console.log('right')
		pacman.x++;
	} else if (e.keyCode == 38 && world[pacman.y-1][pacman.x] !=2) {
		console.log('up')
		pacman.y--;
	} else if (e.keyCode == 40 && world[pacman.y+1][pacman.x] !=2) {
		console.log('down')
		pacman.y++;
	}

	if(world[pacman.y][pacman.x] == 1) {
		world[pacman.y][pacman.x] = 0;
		score += 100;
		displayWorld();
	}
	displayPacman();
	updateScore();
}

function updateScore() {
	document.getElementById('score').innerHTML = score;
}


displayWorld();
displayPacman();